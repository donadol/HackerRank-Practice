/**
*   Return the second largest number in the array.
*   @param {Number[]} nums - An array of numbers.
*   @return {Number} The second largest number in the array.
**/
function getSecondLargest(nums) {
    // Complete the function
    nums.sort((a, b) => b - a);
    let a=nums[0];
    for (let e of nums) {
      if(e!=a)
        return e;
    }
}


